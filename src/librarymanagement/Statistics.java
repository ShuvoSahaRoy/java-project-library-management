    public void issueTable(){
        try{
            String sql = "Select Book_id,Name,Edition,Publisher,Student_id,SName from Issue";
            pst = connection.prepareStatement(sql);
            rs= pst.executeQuery();
            t1.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    public void returnTable(){
        try{
            String sql = "Select Student_id,Name,FName,Course,Branch,Year,DateOfIssue,DOR from Return";
            pst = connection.prepareStatement(sql);
            rs= pst.executeQuery();
            t2.setModel(DbUtils.resultSetToTableModel(rs));
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
    
